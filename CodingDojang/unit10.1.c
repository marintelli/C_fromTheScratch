#include <stdio.h>

int main()
{
    printf("%d \n", 10);   // 10: 정수 리터럴
    printf("%f\n", 0.1f); // float 0.1 
    printf("%c\n", 'a'); // char literal
    printf("%s\n", "Heelo, world!"); // String

    printf("%d\n", 19); // Decimal 19
    printf("0%o\n", 017); // octal number 8
    printf("0x%X\n", 0x1F); //Hexadecimal

    printf("%f\n", 0.1f); //float notaion
    printf("%f\n", 0.1f); //float notation
    printf("%f\n", 1.0e-5f);// exponent notation
    printf("%f\n", 1.0E-5F); // exponent notation

    return 0;
}